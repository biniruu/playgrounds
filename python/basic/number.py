# nonzero numbers are considered True
print(bool(True))
print(bool(1))
print(bool(15))
print(bool(-1))
print(bool(-200))

# zero-valued ones are considered False
print(bool(False))
print(bool(0))
print(bool(0.0))

# remainder
print(9 % 5)

# floating-point division
print(9 / 5)

# integer division
print(9 // 5)

# exponents

print(-5 ** 2)
